
package Muebles;


public class Mueble {
        private String nombre;
        private double precio;

    public Mueble(String nombre, double precio) {
        this.nombre = nombre;
        this.precio = precio;
    }

    public String getNombre() {
        return nombre;
    }

    public double getPrecio() {
        return precio;
    }
        

}
